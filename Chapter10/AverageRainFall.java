public class AverageRainFall
{

    public static void main(String[] args) {
        double aprilRain = 12.0;
        double mayRain = 14.0;
        double juneRain = 8.0;
        double averageRain = (aprilRain + mayRain + juneRain)/3;
        System.out.println("Rainfall for April: "+ aprilRain);
        System.out.println("Rainfall for May: "+ mayRain);
        System.out.println("Rainfall for June: "+ juneRain);
        System.out.println("Average rainfall: "+ averageRain);
    }

}